package main_package.model;

/**
 *
 * @author Adrian
 */
public class Costs_struct_yaml {
    private final String Type;
    private final String Title;
    private final float Cost;
    
    public Costs_struct_yaml(String Type, String Title, float Cost){
        this.Type = Type;
        this.Title = Title;
        this.Cost = Cost;
    }
    
    public String getType(){
        return this.Type;
    }
    
    public String getTitle(){
        return this.Title;
    }
    
    public float getCost(){
        return this.Cost;
    }
}
