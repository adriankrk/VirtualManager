    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row_index = Table_charges.getSelectedRow();
        //System.out.println(row_index);
        int rows_index[] = Table_charges.getSelectedRows();
        //System.out.println(rows_index.length);
        try{ 
            if (row_index==-1 || rows_index.length==0){
                throw new ArrayIndexOutOfBoundsException();
            }
            else{               
                if (row_index!=-1 && rows_index.length==1){ 
                    //System.out.println(row_index);
                    //model_of_table.removeRow(row_index);
                    charges_view.delete(row_index, class_name);
                    //TimeUnit.MILLISECONDS.sleep(500);
                    charges_view.refreshView(model_of_table, class_name);
                }
                else{
                    for (int i=0; i<rows_index.length; i++){
                        //model_of_table.removeRow(rows_index[i]-i);
                        charges_view.delete(rows_index[i]-i, class_name);

                        //System.out.println(rows_index[i]-i);
                    }
                    //TimeUnit.MILLISECONDS.sleep(500);
                    charges_view.refreshView(model_of_table, class_name);
                }
            }           
        }
        catch (ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Wybierz opłatę do usunięcia!!!", "Error", JOptionPane.ERROR_MESSAGE);
        } 
        catch(IndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Wybierz opłatę do usunięcia!!!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        int row_index = Table_charges.getSelectedRow();
        if (row_index==-1){
            JOptionPane.showMessageDialog(null, "Wybierz opłatę do aktualizacji!!!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            Update_index = row_index;
            Pair<javax.swing.JTextField ,javax.swing.JTextField> pair = Pair.with(Type, Cost);
            charges_view.writeDataToTextField(Update_index, class_name, pair);
            
            addButton.setVisible(false);
            updateToList.setVisible(true);
        }        
    }//GEN-LAST:event_updateButtonActionPerformed
                                                                                                                                              
