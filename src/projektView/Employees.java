    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        try{
            String name = Name.getText();
            if (!name.matches("[a-zA-ZąćęśłóżźńĄĆĘÓŁŚŃŻŹ]+") || "".equals(name))
                throw new StringoutException();
            String surname = Surname.getText();
            if (!surname.matches("[a-zA-ZąćęśłóżźńĄĆĘŚÓŁŃŻŹ]+") || "".equals(surname))
                throw new StringoutException();
            if (!Salary.getText().matches("[0-9]+") || "".equals(Salary.getText()) )
                throw new NumberoutException();
            int salary = Integer.parseInt(Salary.getText());
            
            Object selectedItem = jComboBox1.getSelectedItem();
            String selectedItemStr = selectedItem.toString();
            String type = this.convertfromCombotoyamlformat(selectedItemStr);
            employees_view.add(name, surname, salary, type);
            Name.setText("");
            Surname.setText("");
            Salary.setText("");
            //TimeUnit.MILLISECONDS.sleep(500);
            employees_view.refreshView(model_of_table, class_name);
        }
        catch (StringoutException ex) {
            Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Nieprawne dane!!! Pola imię i nazwisko mogą zawierać tylko litery", "Error", JOptionPane.ERROR_MESSAGE);
        } 
        catch (NumberoutException ex) {
            Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Nieprawne dane!!! Pole pensja może zawierać tylko liczby", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed

        int row_index = jTable2.getSelectedRow();
        int rows_index[] = jTable2.getSelectedRows();
        try{ 
            if (row_index==-1 || rows_index.length==0){
                throw new ArrayIndexOutOfBoundsException();
            }
            else{               
                if (row_index!=-1 && rows_index.length==1){ 
                    employees_view.delete(row_index, class_name);
                    //TimeUnit.MILLISECONDS.sleep(500);
                    employees_view.refreshView(model_of_table, class_name);
                }
                else{
                    for (int i=0; i<rows_index.length; i++){
                        employees_view.delete(rows_index[i]-i, class_name);
                    }
                    //TimeUnit.MILLISECONDS.sleep(500);
                    employees_view.refreshView(model_of_table, class_name);
                }
            }           
        }
        catch (ArrayIndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(null, "Wybierz pracownika do usunięcia!!!", "Error", JOptionPane.ERROR_MESSAGE);
        } 
        catch(IndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(null, "Wybierz pracownika do usunięcia!!!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

