    private String convertfromCombotoyamlformat(String param){
        switch (param){
            case "o pracę": 
                return "Praca";
            case "zlecenie":
                return "Zlecenie";
            case "zlecenie (studenci)":
                return "Zlecenie (studenci)";
            case "o dzieło":
                return "Dzieło";
            default:
                return "";
        }
    }
