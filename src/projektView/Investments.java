    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        try{
            String type = Type.getText();
            if (!type.matches("[a-zA-ZąćęśłóżźńĄĆŚĘÓŁŃŻŹ ]+") || "".equals(type))
                throw new StringoutException();    
            if (!Cost.getText().matches("[0-9]+") || "".equals(Cost.getText()))
                throw new NumberoutException();
            int cost = Integer.parseInt(Cost.getText());           
            investments_view.add(type, cost, class_name);
            Type.setText("");
            Cost.setText("");
            investments_view.refreshView(model_of_table, class_name);
        }
        catch (StringoutException ex) {
            Logger.getLogger(Investments.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Nieprawne dane! Pole inwestycja może zawierać tylko litery", "Error", JOptionPane.ERROR_MESSAGE);
        } 
        catch (NumberoutException ex) {
            Logger.getLogger(Investments.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Nieprawne dane! Pole koszt może zawierać tylko liczby", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_addButtonActionPerformed

